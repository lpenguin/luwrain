package org.luwrain.core.registry;

public class Value
{
    public int type = Registry.INVALID;
    public String name = "";
    public int intValue;
    public String strValue = "";
    public boolean boolValue;

    @Override
    public String toString() {
        return "Value{" +
                "type=" + type +
                ", name='" + name + '\'' +
                ", intValue=" + intValue +
                ", strValue='" + strValue + '\'' +
                ", boolValue=" + boolValue +
                '}';
    }
}
